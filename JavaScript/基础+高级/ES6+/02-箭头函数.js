/* 
    1. 当我们的箭头函数只有一行代码的时候 {}可以省略
    2. 当我们的箭头函数只有一行return的时候 return可以省略
    3. 当我们只有一个参数的时候 ()可以省略
    4. 如果我们返回值是对象的话 对象需要使用()进行一个包裹
    5. 箭头函数是没有this的 他的this是当时的变量this 遵循作用域往上找
    6. 箭头函数中的this不会被改变
    7. 箭头函数不能用作构造器
    8. class中的constructor 不能用箭头函数
    9. 箭头函数没有原型对象(没有显示原型，有隐士原型)
    10. 给原型对象添加方法的时候，不推荐去使用箭头函数

*/